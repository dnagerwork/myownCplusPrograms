/**
 * In this file we will see the following functions:
 * 1. Multiply
 * 2. Division
 * 3. Sum
 * 4. Substract
 **/
class basic_operations {
    public: 
        int multiply(int a, int b) {
            return a * b;
        }
        int sum(int a, int b) {
            return a + b;
        }
        int substract(int a, int b) {
            return a - b;
        }
        int divide(int a, int b) {
            return a / b;
        }
};
